#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *ptr;

	ptr = 0;
	if (*s == c)
		ptr = (char *)s;
	while (*s++)
		if (*s == c)
			ptr = (char *)s;
	return (ptr);
}
