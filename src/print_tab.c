#include "minishell.h"

void		print_tab(char **tab)
{
	size_t i;

	i = 0;
	if (tab != NULL)
	{
		while (tab[i] != NULL)
		{
			ft_putstr(tab[i++]);
			ft_putstr("\n");
		}
	}
}
